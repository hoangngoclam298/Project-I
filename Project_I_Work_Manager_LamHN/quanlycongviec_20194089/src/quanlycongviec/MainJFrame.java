    public void updateListPlan(){
        tableModel.setRowCount(0);
        List<Plan> listPlans = new ArrayList<>();
        listPlans = this.user.getListPlans();
        if(!listPlans.isEmpty()){
            for(Plan plan: listPlans){
                tableModel.addRow(new Object[]{plan.getPlan(),plan.getPlace(),
                        plan.getBeginDate(),plan.getEndDate(),plan.getFinish()});
            }
        }
        Main.save();
    }
    
    public void updateListUser(){
        tableEdit.setRowCount(0);
        List<User> listUsers= new ArrayList<>();
        listUsers = this.listUsers.getListUsers();
        if(!(listUsers.isEmpty())){
            for(User user: listUsers){
                tableEdit.addRow(new Object[]{user.getUser(),
                        user.getPassword(),user.getFullName()});
            }
        }
        Main.save();
    }
    
    public void addPlan(Plan planAdd){
        if(!user.addPlan(planAdd)){
            JOptionPane.showMessageDialog(null, "Trùng mốc thời gian với công việc khác");
        }
        updateListPlan();
    }
    
    public boolean addUser(User userAdd){
        if(!listUsers.addUser(userAdd)){
            JOptionPane.showMessageDialog(null, "Trùng tên tài khoản");
            return false;
        }
        updateListUser();
        return true;
    }
    
