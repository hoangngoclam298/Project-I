    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        int hourBegin = jCBHourBegin.getSelectedIndex();
        int minuteBegin = jCBMinuteBegin.getSelectedIndex();
        int dayBegin = jCBDayBegin.getSelectedIndex() + 1;
        int monthBegin = jCBMonthBegin.getSelectedIndex() + 1;
        int yearBegin = Integer.parseInt(jCBYearBegin.getSelectedItem().toString());

        int hourEnd = jCBHourEnd.getSelectedIndex();
        int minuteEnd = jCBMinuteEnd.getSelectedIndex();
        int dayEnd = jCBDayEnd.getSelectedIndex() + 1;
        int monthEnd = jCBMonthEnd.getSelectedIndex() + 1;
        int yearEnd = Integer.parseInt(jCBYearEnd.getSelectedItem().toString());

        LocalDateTime beginDT = null;
        LocalDateTime endDT = null;
        boolean isOK = true;
        int repeatTimes = 1;

        if(jTFPlan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Vui lòng điền đủ thông tin !");
            isOK = false;
        }

        try{
            beginDT = LocalDateTime.of(yearBegin,monthBegin,
                dayBegin,hourBegin,minuteBegin);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Định dạng sai !");
            isOK = false;
        }

        try{
            endDT = LocalDateTime.of(yearEnd,monthEnd,
                dayEnd,hourEnd,minuteEnd);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Định dạng sai !");
            isOK = false;
        }
        
        if(!beginDT.isBefore(endDT)){
            JOptionPane.showMessageDialog(null, "Ngược thời gian !");
            isOK = false;
        }
        
        try {
            if(!jRBNoRepeat.isSelected()){
                repeatTimes = Integer.parseInt(jTFRepeatTimes.getText());
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Sai định dạng số lần lặp");
            isOK = false;
        }
        
        if(isOK){
            String planName = jTFPlan.getText();
            String place = jTFPlace.getText();
            if(jRBNoRepeat.isSelected()){
                Plan plan = new Plan(planName, beginDT, endDT, place,false);
                this.addPlan(plan);
                return;
            }
            if(jRBDay.isSelected()){
                for(int i = 0; i < repeatTimes; i++){
                    Plan plan = new Plan(planName, beginDT.plusDays(i), 
                            endDT.plusDays(i), place,false);
                    this.addPlan(plan);
                }
                return;
            }
            if(jRBWeek.isSelected()){
                for(int i = 0; i < repeatTimes; i++){
                    Plan plan = new Plan(planName, beginDT.plusWeeks(i), 
                            endDT.plusWeeks(i), place,false);
                    this.addPlan(plan);
                }
                return;
            }
            if(jRBMonth.isSelected()){
                for(int i = 0; i < repeatTimes; i++){
                    Plan plan = new Plan(planName, beginDT.plusMonths(i), 
                            endDT.plusMonths(i), place,false);
                    this.addPlan(plan);
                }
                return;
            }
            if(jRBYear.isSelected()){
                for(int i = 0; i < repeatTimes; i++){
                    Plan plan = new Plan(planName, beginDT.plusYears(i), 
                            endDT.plusYears(i), place,false);
                    this.addPlan(plan);
                }
            }
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

