            jCBHourEnd1.setSelectedIndex(tmp.getEndDate().getHour());
            jCBMinuteEnd1.setSelectedIndex(tmp.getEndDate().getMinute());
            jCBDayEnd1.setSelectedIndex(tmp.getEndDate().getDayOfMonth()-1);
            jCBMonthEnd1.setSelectedIndex(tmp.getEndDate().getMonthValue()-1);
